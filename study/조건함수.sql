-- CASE 함수
--동등비교 : 일치하면 THEN 뒤의 문장 실행, 모두 불일치면 ELSE뒤의 문장 실행
SELECT last_name,salary, CASE salary WHEN 24000 THEN salary*0.3
                                     WHEN 17000 THEN salary*0.2
                                     ELSE salary 
                         END 보너스 FROM employees
ORDER BY 2 desc;
-- 부등비교
SELECT last_name,salary, -- 비교연산자
       CASE WHEN salary >=20000 THEN 1000
            WHEN salary >=15000 THEN 2000
            WHEN salary >=10000 THEN 3000
            ELSE 4000 
       END 보너스 FROM employees
ORDER BY 2 desc;

SELECT last_name,salary, -- BETWEEN a AND b
       CASE WHEN salary BETWEEN 20000 AND 25000 THEN '상'
            WHEN salary BETWEEN 10000 AND 20001 THEN '중'
            ELSE '하'
       END 등급 FROM employees
ORDER BY 2 desc;

SELECT last_name,salary, -- IN
       CASE WHEN salary IN ( 24000, 17000 , 14000) THEN '상'
